  int16_t waterpressureADC = (i2cdataresponse[0] << 8) + i2cdataresponse[1];
  double waterVoltage = waterpressureADC * 5.0 / 32767.0;
  //0.5V = 0psi ///// 4.5V = 15psi
  double waterPSI = ((waterVoltage - 0.44) / 4.0) * 15;
  //height in inches
